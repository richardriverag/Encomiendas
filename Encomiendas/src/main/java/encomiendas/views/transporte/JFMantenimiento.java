    private void limpiarCampos() {
        txtDescripcion.setText("");
        txtIdTransporte.setText("");
        jDCFechaManten.setDate(null);
    }

    private void cargarTabla(Mantenimiento mantenimiento) {
        modeloTablaConsulta = new DefaultTableModel() {
            //filas y columnas no sean editables
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        String titulos[] = {"ID", "Fecha", "Descripción", "Transporte ID"};
        //seteamos las columnas
        modeloTablaConsulta.setColumnIdentifiers(titulos);
        Object[] objeto = {mantenimiento.getId(), mantenimiento.getFecha(), mantenimiento.getDescripcion(), mantenimiento.getTransporteId()};
        // Agregar la fila al modelo de la tabla
        modeloTablaConsulta.addRow(objeto);
        // Establecer el modelo de la tabla
        tablaConsulta.setModel(modeloTablaConsulta);
    }

    private void cargarMantCompletas() {
        try {
            DefaultTableModel modeloTabla = new DefaultTableModel() {
                //filas y columnas no sean editables
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            //nombres de las columnas
            String titulos[] = {"ID", "Fecha", "Descripción", "Transporte ID"};
            //seteamos las columnas
            modeloTabla.setColumnIdentifiers(titulos);
            List<Mantenimiento> listaMantenimiento = manController.listarMantenimientos();
            if (listaMantenimiento != null) {
                for (Mantenimiento mantenimiento : listaMantenimiento) {
                    Object[] objeto = {mantenimiento.getId(), mantenimiento.getFecha(), mantenimiento.getDescripcion(), mantenimiento.getTransporteId()};
                    modeloTabla.addRow(objeto);
                }
            }

            tablaManteCom.setModel(modeloTabla);
        } catch (SQLException ex) {
            Logger.getLogger(JFTransporte.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void setearMantenimiento(int idMatActualizar) {
        try {
            Mantenimiento mantenimiento = manController.obtenerMantenimiento(idMatActualizar);
            txtDescripcioned.setText(mantenimiento.getDescripcion());
            txtIdTransporteEd.setText(String.valueOf(mantenimiento.getTransporteId()));
            jDCFechaMantenEd.setDate(mantenimiento.getFecha());
        } catch (SQLException ex) {
            Logger.getLogger(JFMantenimiento.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    // Método para limpiar la tabla

    private void limpiarTabla() {
        // Si la tabla ya tiene un modelo de tabla, limpiar todas las filas
        if (modeloTablaConsulta != null) {
            modeloTablaConsulta.setRowCount(0);
        }
    }

