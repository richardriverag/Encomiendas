    private void btnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminar1ActionPerformed
        // TODO add your handling code here:
        if (tablaManteCom.getRowCount() > 0) {
            //se haya seleccionado una fila dado que se puede seleccionar de 0 registros en adelante
            if (tablaManteCom.getSelectedRow() != -1) {
                try {
                    int idMantEliminar = Integer.parseInt(String.valueOf(tablaManteCom.getValueAt(tablaManteCom.getSelectedRow(), 0)));
                    manController.eliminarMantenimiento(idMantEliminar);
                    JOptionPane.showMessageDialog(null, "Mantenimiento eliminado correctamente", "Información", JOptionPane.INFORMATION_MESSAGE);
                    cargarMantCompletas();

                } catch (SQLException ex) {
                    Logger.getLogger(JFRuta.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se selecciono ningun Mantenimiento", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No hay nada para eliminar en la tabla", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminar1ActionPerformed

    private void btnEditar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditar1ActionPerformed
        // TODO add your handling code here:
        if (tablaManteCom.getRowCount() > 0) {
            if (tablaManteCom.getSelectedRow() != -1) {
                jTabbedPane1.setEnabledAt(3, true);
                jTabbedPane1.setSelectedIndex(3);
                jTabbedPane1.setEnabledAt(0, false);
                jTabbedPane1.setEnabledAt(1, false);
                jTabbedPane1.setEnabledAt(2, false);

                idMatActualizar = Integer.parseInt(String.valueOf(tablaManteCom.getValueAt(tablaManteCom.getSelectedRow(), 0)));
                setearMantenimiento(idMatActualizar);

            } else {
                JOptionPane.showMessageDialog(null, "No se selecciono ningun Mantenimiento", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "No hay nada para editar en la tabla", "Advertencia", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnEditar1ActionPerformed

